package com.gottagrindemall.compat.jade;

import com.gottagrindemall.blocks.MobSpawningBlock;
import snownee.jade.api.IWailaClientRegistration;
import snownee.jade.api.IWailaCommonRegistration;
import snownee.jade.api.IWailaPlugin;
import snownee.jade.api.WailaPlugin;

@WailaPlugin
public class JadePlugin implements IWailaPlugin {
  @Override
  public void register(IWailaCommonRegistration registration) {
    // No server-side data providers needed
  }

  @Override
  public void registerClient(IWailaClientRegistration registration) {
    registration.registerBlockComponent(MonDirtComponentProvider.INSTANCE, MobSpawningBlock.class);
  }
}
